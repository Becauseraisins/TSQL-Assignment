BEGIN
INSERT INTO CUSTOMER(CUSTID, CUSTNAME, SALES_YTD, STATUS) VALUES (@NEWCUSTID, @NEWCUSTNAME, 0, OK)
END

GO 
CREATE PROCEDURE DELETE_ALL_CUSTOMERS AS
BEGIN
DELETE FROM CUSTOMER (CUSTID, CUSTNAME, SALES_YTD, STATUS) 
END

GO 
